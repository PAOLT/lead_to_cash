# ----- Helper functions -----
import pandas as pd
from typing import Optional, Tuple, Dict, Any

def run_sql(sql: str) -> pd.DataFrame:
    df = spark.sql(sql)
    return df.limit(TOP_N_COMPARE).toPandas()

def load_agent_handle(name: str):
    # Try common preview SDK variants
    try:
        from fabric_data_agent_sdk import DataAgentClient
        client = DataAgentClient()
        try:
            return client.get_or_create_agent(name=name)
        except Exception:
            return client.create_agent(name=name)
    except Exception:
        pass
    try:
        from fabric.dataagent.client import create_data_agent
        return create_data_agent(name)
    except Exception:
        raise RuntimeError('Fabric Data Agent SDK not found. Ensure pip install ran and you are in Fabric.')

def ask_agent(agent, question: str) -> Tuple[str, Optional[str]]:
    """Return (answer_text, agent_sql_if_any). SDKs vary; we try common methods and inspect details."""
    methods = ['ask','query','chat','run','ask_question']
    result = None
    for m in methods:
        try:
            fn = getattr(agent, m)
            result = fn(question)
            break
        except Exception:
            continue
    if result is None:
        try:
            from fabric_data_agent_sdk import DataAgentClient
            client = DataAgentClient()
            result = client.ask(agent, question)
        except Exception:
            pass
    text = None
    if isinstance(result, str):
        text = result
    else:
        for k in ['answer','text','content','message','output']:
            try:
                v = result.get(k) if hasattr(result,'get') else getattr(result,k,None)
                if isinstance(v,str) and v.strip():
                    text = v
                    break
            except Exception:
                pass
    sql = None
    candidates = ['sql','generated_sql','data_retrieval_query','sql_queries']
    if hasattr(result,'get'):
        for k in candidates:
            try:
                v = result.get(k)
                if isinstance(v,str) and 'select' in v.lower():
                    sql = v; break
                if isinstance(v,list) and v and isinstance(v[0],str) and 'select' in v[0].lower():
                    sql = v[0]; break
            except Exception:
                pass
    if sql is None:
        for detail_m in ['get_run_details','details','last_run_details']:
            try:
                d = getattr(agent, detail_m)()
                for k in candidates:
                    try:
                        v = d.get(k) if hasattr(d,'get') else None
                        if isinstance(v,str) and 'select' in v.lower():
                            sql = v; break
                        if isinstance(v,list) and v and isinstance(v[0],str) and 'select' in v[0].lower():
                            sql = v[0]; break
                    except Exception:
                        pass
                if sql: break
            except Exception:
                pass
    return (text or ''), sql

def compare_frames(df_gt: pd.DataFrame, df_agent: pd.DataFrame) -> Dict[str, Any]:
    res = {
        'rowcount_gt': len(df_gt), 'rowcount_agent': len(df_agent),
        'colnames_gt': list(df_gt.columns), 'colnames_agent': list(df_agent.columns),
    }
    set_gt, set_ag = set(df_gt.columns), set(df_agent.columns)
    inter = set_gt & set_ag
    union = set_gt | set_ag
    res['col_jaccard'] = (len(inter)/len(union)) if union else 1.0
    diffs = []
    for c in inter:
        if pd.api.types.is_numeric_dtype(df_gt[c]) and pd.api.types.is_numeric_dtype(df_agent[c]):
            a = df_gt[c].fillna(0).astype(float)
            b = df_agent[c].fillna(0).astype(float)
            n = min(len(a), len(b), 50)
            if n>0:
                diffs.append(float((a.head(n)-b.head(n)).abs().mean()))
    res['avg_abs_diff_shared_num'] = (sum(diffs)/len(diffs)) if diffs else None
    res['pass_flag'] = int( (res['col_jaccard'] >= 0.6) and (abs(res['rowcount_gt']-res['rowcount_agent']) <= max(5, 0.1*max(1,res['rowcount_gt']))) )
    return res


